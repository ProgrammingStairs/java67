// program showing the concept of shorthand operator

class Demo12{
    public static void main(String args[]){
        int a = 100, b = 625;
        a += 20;   // a = a + 20;
        System.out.println("Value of a : "+a);

        b /= 25;   // b = b / 25;
        System.out.println("Value of b : "+b);
    }
}
