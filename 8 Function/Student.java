class Student{
    private int rno;
    private double per;
    private String name;

    // setter methods
    void setRno(int rno){
        this.rno=rno;
    }
    void setPer(double per){
        this.per=per;
    }
    void setName(String name){
        this.name=name;
    }
    
    // getter methods
    int getRno(){
        return rno;
    }
    double getPer(){
        return per;
    }
    String getName(){
        return name;
    }
}
